    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);
        ArrayList<VisitRequest> iq = system.getVisitQueue().getVisitQueue();
        String agent = userAccount.getUsername();
        curriq = new ArrayList<>();
        for (VisitRequest req : iq) {
            if (req.getAgentComment() != null && req.getAgentComment().equals(agent)) {
                Object[] row = new Object[5];
                row[0] = req.getUserName();
                row[1] = req.getStatus();
                model.addRow(row);
                curriq.add(req);
            }
        }
    }

