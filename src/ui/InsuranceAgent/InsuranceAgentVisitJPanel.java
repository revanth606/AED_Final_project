    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Request not selected");
            return;
        }
        VisitRequest vr = curriq.get(selectedRow);
        vr.setStatus("Insurance rejected");
        JOptionPane.showMessageDialog(this, "Insurance rejected");
        vr.setStatus("Insurance approved");
        JOptionPane.showMessageDialog(this, "Insurance approved");
        
    }//GEN-LAST:event_assignJButtonActionPerformed

